"""Minimal key-based i18n layer: EN/FR string catalog plus a QSettings-backed
language preference. Language is read once at process start (see
`current_language`); switching it in Settings takes effect after restart.
"""
from __future__ import annotations

from PyQt6.QtCore import QSettings

_ORG = "SeilioDouar"
_APP = "SeilioBilling"

_CURRENT: str | None = None


def get_saved_language() -> str:
    settings = QSettings(_ORG, _APP)
    return settings.value("language", "fr")


def set_saved_language(lang: str) -> None:
    settings = QSettings(_ORG, _APP)
    settings.setValue("language", lang)


def current_language() -> str:
    global _CURRENT
    if _CURRENT is None:
        _CURRENT = get_saved_language()
    return _CURRENT


